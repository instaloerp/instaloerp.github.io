// Devuelve el índice de columna ΔT (4 columnas Jaga: [45/40, 50/45, 55/45, 75/65])
// Para uso "exacto" (cuando el ΔT cae justo en una columna). Si está entre, prefiere interpolación.
function jagaDtIndex(dt) {
  if (dt <= 23) return 0;
  if (dt <= 28) return 1;
  if (dt <= 35) return 2;
  return 3;
}

// ── Cálculo de potencia mediante el EXPONENTE N (EN442) ────────
// Formula: P_real = P_ref × (ΔT_real / ΔT_ref)^n
// Donde n se deriva de dos puntos conocidos del catálogo:
//   n = ln(P1/P2) / ln(ΔT1/ΔT2)
// Valores típicos: aluminio 1.25-1.30 · acero/BT 1.30-1.35 · fundición 1.28
//
// Aplicado a Jaga (4 cols): tomamos la pareja 30 y 50 para derivar n
// y extrapolamos con la fórmula a cualquier ΔT.
function jagaPotenciaInterpolada(arr, dt) {
  if (!arr) return 0;
  const dtCols = [22.5, 27.5, 30, 50];
  // Si cae en una columna exacta, devolver directamente
  for (let i = 0; i < 4; i++) {
    if (Math.abs(dt - dtCols[i]) < 0.01) return arr[i];
  }
  // Derivar n de los dos puntos más estables (col 30 y col 50)
  const p30 = arr[2], p50 = arr[3];
  if (!p30 || !p50) return arr[3];
  const n = Math.log(p30 / p50) / Math.log(30 / 50);
  // P_real = P_ref × (ΔT_real / ΔT_ref)^n   (usando ΔT 50 como ref)
  return Math.round(p50 * Math.pow(dt / 50, n));
}

// Calcula el exponente N de un radiador a partir de dos puntos
function exponenteN(p1, dt1, p2, dt2) {
  if (!p1 || !p2 || dt1 === dt2) return 1.30;
  return Math.log(p1 / p2) / Math.log(dt1 / dt2);
}

// Calcula potencia a un ΔT objetivo usando un punto de referencia y un exponente
function potenciaConN(p_ref, dt_ref, dt_obj, n) {
  if (!p_ref) return 0;
  return Math.round(p_ref * Math.pow(dt_obj / dt_ref, n || 1.30));
}

// ── DBH / DBE (Dynamic Boost Hybrid) — set ventilador opcional Jaga ────
// Multiplica la potencia del radiador estático según el ΔT.
// Factor más alto a baja temperatura (ideal BdC), más bajo a alta temperatura.
// Valores aproximados publicados por Jaga (Strada Hybrid · Tempo DBH · Play DBE):
//   ΔT 15 → ×3.5     ΔT 22.5 → ×2.7     ΔT 30 → ×2.2     ΔT 50 → ×1.6
function factorDBH(dt) {
  if (dt <= 15) return 3.5;
  if (dt >= 50) return 1.6;
  // Interpolación lineal entre extremos
  return +(3.5 - (dt - 15) * (3.5 - 1.6) / (50 - 15)).toFixed(2);
}

// Devuelve el índice de columna Aquabit ([22.5, 30, 40])
function aquabitDtIndex(dt) {
  if (dt <= 25) return 0;
  if (dt <= 35) return 1;
  return 2;
}

// Aquabit: usa el exponente N derivado de las cols 22.5 y 40 para extrapolar a cualquier ΔT
function aquabitPotenciaInterpolada(arr, dt) {
  if (!arr) return 0;
  const dtCols = [22.5, 30, 40];
  for (let i = 0; i < 3; i++) {
    if (Math.abs(dt - dtCols[i]) < 0.01) return arr[i];
  }
  const p22 = arr[0], p40 = arr[2];
  if (!p22 || !p40) return arr[1] || arr[2];
  const n = Math.log(p22 / p40) / Math.log(22.5 / 40);
  return Math.round(p40 * Math.pow(dt / 40, n));
}

// Temperatura de impulsión predeterminada según generador (ºC)
//   · BdC aire/agua + geotermia: 45º MÁX (no más alto, daña COP)
//   · Caldera (gas/gasoil) + biomasa: 60º predeterminado
function impulsionRecomendada(generadorTipo) {
  if (generadorTipo === 'bdc_aire_agua' || generadorTipo === 'geotermia' || generadorTipo === 'aerotermia') return 45;
  return 60; // caldera + biomasa
}

// Salto hidráulico estándar (T_imp − T_ret) según generador (ºC)
//   · BdC: 5º   · Caldera/biomasa: 10º
function saltoHidraulicoEstandar(generadorTipo) {
  if (generadorTipo === 'bdc_aire_agua' || generadorTipo === 'geotermia' || generadorTipo === 'aerotermia') return 5;
  return 10;
}

// Temperatura ambiente de diseño RITE (zona habitable invierno = 21ºC)
const T_AMBIENTE_RITE_DEFAULT = 21;

// ΔT medio del catálogo a partir de T. impulsión y T. ambiente
//   ΔT_m = (T_imp + T_ret) / 2 - T_amb     con T_ret = T_imp − salto_hidráulico
//   Devuelve la columna más cercana del catálogo: 22.5 / 27.5 / 30 / 50
function deltaTDesdeTemperaturas(tImp, tAmb, saltoHidr) {
  if (tAmb == null) tAmb = T_AMBIENTE_RITE_DEFAULT;
  if (saltoHidr == null) saltoHidr = (tImp <= 47) ? 5 : 10;
  const tRet = tImp - saltoHidr;
  const dtReal = (tImp + tRet) / 2 - tAmb;
  // Mapear al ΔT más cercano del catálogo
  if (dtReal <= 25) return 22.5;
  if (dtReal <= 28.75) return 27.5;
  if (dtReal <= 40) return 30;
  return 50;
}

// ΔT real numérico (sin redondear a columna catálogo)
function deltaTReal(tImp, tAmb, saltoHidr) {
  if (tAmb == null) tAmb = T_AMBIENTE_RITE_DEFAULT;
  if (saltoHidr == null) saltoHidr = (tImp <= 47) ? 5 : 10;
  const tRet = tImp - saltoHidr;
  return +((tImp + tRet) / 2 - tAmb).toFixed(1);
}

// Compat: ΔT recomendado según generador (RITE 21ºC)
function deltaTRecomendado(generadorTipo) {
  return deltaTDesdeTemperaturas(impulsionRecomendada(generadorTipo), T_AMBIENTE_RITE_DEFAULT, saltoHidraulicoEstandar(generadorTipo));
}

// Compat: ΔT desde solo impulsión (asume RITE 21º + salto estándar)
function deltaTDesdeImpulsion(tImp) {
  const salto = (tImp <= 47) ? 5 : 10;
  return deltaTDesdeTemperaturas(tImp, T_AMBIENTE_RITE_DEFAULT, salto);
}

// Busca la configuración Jaga MÁS PEQUEÑA que cubra `potencia_w` en un modelo+altura
// tipoFijo: si se pasa, solo varía longitud manteniendo ese tipo. Si null/undefined → "Auto".
//   En modo Auto, prioriza MENOR sobredimensionamiento (no la longitud más corta).
// dbh: aplica el factor DBH/DBE (multiplica la potencia del radiador estático).
function buscarMinimoJaga(modelo, alturaKey, dt, potencia_w, tipoFijo, dbh) {
  const altura = modelo.alturas[alturaKey];
  if (!altura) return null;
  const longs = Object.keys(altura.potencias).sort((a,b) => parseInt(a) - parseInt(b));
  const auto = !tipoFijo;
  const tiposBuscar = tipoFijo ? [tipoFijo] : altura.tipos_disponibles;
  const fDBH = dbh ? factorDBH(dt) : 1;
  let mejor = null;
  for (const lon of longs) {
    for (const tipo of tiposBuscar) {
      const arr = altura.potencias[lon]?.[tipo];
      if (!arr) continue;
      const wEstatico = jagaPotenciaInterpolada(arr, dt);
      const w = Math.round(wEstatico * fDBH);
      if (w >= potencia_w) {
        const sobre = ((w - potencia_w) / potencia_w);
        // En modo Auto: el score es el sobredimensionamiento absoluto (queremos minimizarlo)
        // En modo tipo fijo: minimizar longitud (lo que ya hacíamos)
        const score = auto ? sobre : (parseInt(lon) * 100 + parseInt(tipo));
        if (!mejor || score < mejor.score) {
          mejor = {
            longitud: lon, tipo, potencia_real: w,
            potencia_estatica: wEstatico,
            factor_dbh: fDBH,
            sobre_dim_pct: Math.round(sobre * 100),
            score,
          };
        }
      }
    }
  }
  return mejor;
}

// Busca el ancho Aquabit MÁS PEQUEÑO que cubra (con interpolación por exponente N)
function buscarMinimoAquabit(modeloKey, dt, potencia_w) {
  const m = AQUABIT.modelos[modeloKey];
  if (!m) return null;
  const anchos = Object.keys(m.anchos).map(Number).sort((a,b)=>a-b);
  for (const a of anchos) {
    const w = aquabitPotenciaInterpolada(m.anchos[a], dt);
    if (w >= potencia_w) {
      return {
        ancho: a, potencia_real: w,
        sobre_dim_pct: Math.round(((w - potencia_w) / potencia_w) * 100),
      };
    }
  }
  const aMax = anchos[anchos.length - 1];
  const wMax = aquabitPotenciaInterpolada(m.anchos[aMax], dt);
  return {
    ancho: aMax, potencia_real: wMax,
    sobre_dim_pct: Math.round(((wMax - potencia_w) / potencia_w) * 100),
    insuficiente: true,
  };
}

// Busca nº mínimo de elementos (Baxi aluminio) para una potencia dada
// Usa el exponente N OFICIAL del fabricante (guardado en altura.n) con la fórmula EN-442:
//   P_real = P_ref × (ΔT_real / ΔT_ref)^n        usando ΔT 50 como referencia
function buscarMinimoBaxi(modeloKey, alturaKey, dt, potencia_w) {
  const m = BAXI.modelos[modeloKey];
  if (!m) return null;
  const altura = m.alturas[alturaKey];
  if (!altura) return null;
  // Si dt cae en una columna tabulada, usar el valor directo del fabricante
  let wEl = altura.w[Math.round(dt)];
  if (!wEl) {
    // Usar el exponente N oficial. Si no está, derivarlo de cols 30 y 50.
    const p50 = altura.w[50];
    let n = altura.n;
    if (!n) {
      const p30 = altura.w[30];
      n = (p30 && p50) ? exponenteN(p30, 30, p50, 50) : 1.30;
    }
    if (!p50) return null;
    wEl = potenciaConN(p50, 50, dt, n);
  }
  if (!wEl) return null;
  const elementos = Math.max(2, Math.ceil(potencia_w / wEl));
  return {
    elementos, w_real: Math.round(elementos * wEl), w_elem: +wEl.toFixed(1),
    n_oficial: altura.n || null,
    sobre_dim_pct: Math.round(((elementos * wEl - potencia_w) / potencia_w) * 100),
  };
}

